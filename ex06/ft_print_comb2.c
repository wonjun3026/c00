#include <unistd.h>

void	number(int i, int k)
{
	char	a;
	char	b;
	char	c;
	char	d;

	if (i < k)
	{
		a = i / 10 + '0';
		b = i % 10 + '0';
		c = k / 10 + '0';
		d = k % 10 + '0';
		write (1, &a, 1);
		write (1, &b, 1);
		write (1, " ", 1);
		write (1, &c, 1);
		write (1, &d, 1);
		if (!(a == '9' && b == '8' && c == '9' && d == '9'))
			write (1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int	i;
	int	k;

	i = 0;
	while (i < 99)
	{
		while (k <= 99)
		{
			number(i, k);
			k++;
		}
		k = 0;
		i++;
	}
}
